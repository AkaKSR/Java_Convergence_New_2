-- 13. 매니저가 없는 사람들의 LAST_NAME 및 JOB_ID를 조회한다.
SELECT
	FIRST_NAME || ' ' || LAST_NAME 이름, JOB_ID
FROM
	EMPLOYEES
WHERE
	MANAGER_ID IS NULL;
	
-- 14. 커미션을 버는 모든 사원들의 LAST_NAME, 연봉 및 커미션을 조회한다.
-- 연봉 역순, 커미션 역순차로 정렬한다.
SELECT
	LAST_NAME, SALARY, COMMISSION_PCT
FROM
	EMPLOYEES
WHERE
	COMMISSION_PCT IS NOT NULL
ORDER BY
	SALARY DESC, COMMISSION_PCT DESC;

