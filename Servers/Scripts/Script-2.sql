	SUBSTR(HIRE_DATE,5,2) = SUBSTR(SYSDATE,5,2);

-- 5. 사원 테이블에서 직업이 SA_MAN 중에서 사원번호의 최대값을 출력
SELECT
	MAX(EMPLOYEE_ID) 사원번호
FROM
	EMPLOYEES
WHERE
	JOB_ID='SA_MAN';

SELECT
	MAX(EMPLOYEE_ID)
FROM
	EMPLOYEES
GROUP BY
	JOB_ID
HAVING
	JOB_ID='SA_MAN';
	
