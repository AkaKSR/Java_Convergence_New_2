-- 8. 연봉이 5000에서 12000의 번위이외인 사람들의 LAST_NAME 및 연봉을 조회한다.
SELECT
	LAST_NAME, SALARY
FROM
	EMPLOYEES
WHERE
	SALARY BETWEEN 5000 AND 12000;
-- 9. 1998/02/20부터 1998/05/01 사이에 고용된 사원들의 LAST_NAME, 사번, 고용일자를 고용일자 순으로 정렬한다.
SELECT
	LAST_NAME, EMPLOYEE_ID, HIRE_DATE
FROM
	EMPLOYEES
WHERE
	HIRE_DATE
BETWEEN
	'2002/02/20' AND '2005/01/01'
ORDER BY
	HIRE_DATE;
-- 10. 20번 및 50번 부서에서 근무하는 모든 사원들의 LAST_NAME 및 부서 번호를 알파벳순으로 조회한다.
SELECT
	LAST_NAME, DEPARTMENT_ID
FROM
	EMPLOYEES
WHERE
	DEPARTMENT_ID IN (20,50)
ORDER BY
	LAST_NAME;
-- 11. 20번 및 50번 부서에 근무하며, 연봉이 5000 ~ 12,000 사이인 사원들의 LAST_NAME 및 연봉을 조회한다.
SELECT
	LAST_NAME, SALARY
FROM
	EMPLOYEES
WHERE
	DEPARTMENT_ID IN (20,50) AND SALARY BETWEEN 5000 AND 12000;
-- 12. 2002년도에 고용된 모든 사람들의 LAST_NAME 및 고용일을 조회한다.
