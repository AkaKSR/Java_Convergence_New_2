-- 유사문제 : 이번달에 입사한 사원의 이름과 입사일을 출력
SELECT
	FIRST_NAME || ' ' || LAST_NAME 이름, HIRE_DATE 입사일
FROM
	EMPLOYEES
WHERE
	EXTRACT(MONTH FROM HIRE_DATE)=EXTRACT(MONTH FROM SYSDATE);

SELECT
