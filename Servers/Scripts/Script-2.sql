-- 1. 사원 테이블에서 사원번호가 100, 107 번인 사원번호와 이름을 출력
SELECT
	EMPLOYEE_ID, FIRST_NAME || ' ' || LAST_NAME
FROM
	EMPLOYEES
WHERE
	EMPLOYEE_ID IN ('100', '107');
	
SELECT
	EMPLOYEE_ID, FIRST_NAME || ' ' || LAST_NAME
FROM
	EMPLOYEES
WHERE
	EMPLOYEE_ID=100 OR EMPLOYEE_ID=107;
	
-- 2. 사원 테이블에서 사원번호가 100, 107 번이 아닌 사원번호와 이름을 출력
SELECT
	EMPLOYEE_ID, FIRST_NAME || ' ' || LAST_NAME
FROM
	EMPLOYEES
WHERE
	EMPLOYEE_ID NOT IN ('100', '107');
	
SELECT
	EMPLOYEE_ID, FIRST_NAME || ' ' || LAST_NAME
FROM
	EMPLOYEES
WHERE
	EMPLOYEE_ID!=100 AND EMPLOYEE_ID!=107;
	
-- 3. 사원 테이블에서 급여(SAL)가 3000에서 5000사이인 사원 정보를 출력
SELECT
	*
FROM
	EMPLOYEES
WHERE
	SALARY BETWEEN 3000 AND 5000;
	
SELECT
	*
